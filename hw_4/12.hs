-- -----/ a) map /-----

unzip' :: [(a, b)] -> ([a], [b])
unzip' l = (map fst l, map snd l)


-- -----/ b) foldr /-----

unzip'' :: [(a, b)] -> ([a], [b])
unzip'' = foldr (\(x,y) (xs,ys) -> (x:xs,y:ys)) ([],[])


